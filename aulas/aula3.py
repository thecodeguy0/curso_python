# Tipo str(string) -Introdução aos tipos de dados

"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Olá, mundo!')

# Aspas duplas

print("Olá, mundo!")

# Escape
print("Olá \"mundo!") # A barra invertida serve como nosso caracte de escape, informando ao python para pular o próximo caractere e com isso não interpretando o mesmo.
print('Olá "mundo!"') # Para exibir nossa aspas duplas na palavra mundo, basta colocá-la dentro de aspas simples ou vice-versa.

# r
print(r"Olá \"mundo!")