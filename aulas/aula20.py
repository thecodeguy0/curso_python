# Operadores in e not in
# Strings são iteráveis (Significa que você pode navegar pela string item por item)
# 0 1 2 3 4 5
# B a n a n a
# -6 -5 -4 -3 -2 -1
nome = 'Banana'
print(nome[2])
print(nome[-4])

print('B' in nome)
print('zero' in nome)
print(10 * '-')
print('ana' not in nome)
print('zero' not in nome)