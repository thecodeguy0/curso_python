# Concatenação(+) e repetição(*) com operadores aritméticos

concatenacao = 'A' + 'B' + 'C' # O sinal de + pode fazer tanto adição como concatenação.
print(concatenacao) 

a_dez_vezes = 'A' * 10 # O sinal de multiplicação * também pode fazer a repetição.
tres_vezes_batata = 3 * 'batata'
print(a_dez_vezes)
print(tres_vezes_batata)